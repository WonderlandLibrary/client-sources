package de.dietrichpaul.clientbase.injection.mixin.event;

import de.dietrichpaul.clientbase.event.Render2DListener;
import de.dietrichpaul.clientbase.ClientBase;
import net.minecraft.client.gui.DrawContext;
import net.minecraft.client.gui.hud.InGameHud;
import net.minecraft.client.util.math.MatrixStack;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(InGameHud.class)
public class InGameHudMixin {

    @Inject(method = "render", at = @At("TAIL"))
    public void onPostRender(DrawContext context, float tickDelta, CallbackInfo ci) {
        ClientBase.INSTANCE.getEventDispatcher().post(new Render2DListener.Render2DEvent(context.getMatrices(), tickDelta));
    }
}
